    private void btn_detailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_detailActionPerformed
        // TODO add your handling code here:
        int tabel_data = table_laporan_pengembalian.getSelectedRow();        
        if(String.valueOf(tabel_data).equals("-1")){
            JOptionPane.showMessageDialog(this, "Pilih Data Yang Akan Ditampilkan !");
        }else{
            detail_laporan_pengembalian.public_no_peminjaman = tabMode.getValueAt(table_laporan_pengembalian.getSelectedRow(), 0).toString();
            detail_laporan_pengembalian frameform = new detail_laporan_pengembalian();
            frameform.setVisible(true);
        }
    }//GEN-LAST:event_btn_detailActionPerformed

    private void hitungData() {
        int total_baris = table_laporan_pengembalian.getRowCount();
        if(String.valueOf(total_baris).length()==1){
            hitung_data.setText(""+total_baris);
        }
    }
