    public void tampilDatabase() {
        Object[] baris = {"No Peminjaman", "NIM", "Nama Anggota", "Tanggal Kembali", "Total Denda"}; 
        tabMode = new DefaultTableModel(null, baris); 
        table_laporan_pengembalian.setModel(tabMode);
            try {
               hasil = crud.tampilData();
                while (hasil.next()){
                    String[] data = {
                        hasil.getString("no_peminjaman"),
                        hasil.getString("nim"),
                        hasil.getString("nama_anggota"),
                        hasil.getString("tanggal_kembali"),
                        hasil.getString("total_denda")
                    };
                    crud.setDataPengembalian(data);
                    tabMode.addRow(crud.getDataPengembalian());
                }
            } catch (Exception e) {
            }      
    }
