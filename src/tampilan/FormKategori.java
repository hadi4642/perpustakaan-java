    public void tampilDatabase() {
        int kd = 0;
        for (int i = 0; i < 10; i++) {
            kd++;
        }
        
        Object[] baris = {"id", "Nama Kategori"}; 
        tabMode = new DefaultTableModel(null, baris); 
        tabel_kategori.setModel(tabMode);
            try {
               hasil = crud.tampilData();
                while (hasil.next()){
                    String[] data = {
                        hasil.getString("id"),
                        hasil.getString("nama_kategori"),
                    };
                    crud.setDataKategori(data);
                    tabMode.addRow(crud.getDataKategori());
                }
            } catch (Exception e) {
            }      
    }
    
