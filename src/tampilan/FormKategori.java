    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        if (txt_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Nama Kategori belum diisi !"); txt_id.requestFocus();
        } else if(txt_nama_kategori.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Nama Kategori belum diisi !"); txt_nama_kategori.requestFocus();
        }else {
            try {
                String[] setData = {
                    txt_nama_kategori.getText(),
                    txt_id.getText(),
                };
                crud.setDataKategori(setData);
                crud.ubahData(crud.getDataKategori());
                JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                tampilDatabase();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);

            }
        }

    }//GEN-LAST:event_btn_ubahActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        if (txt_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Id belum diisi !"); txt_id.requestFocus();
        } 
        else {
        if (JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ini ?", "Warning", 2) == JOptionPane.YES_OPTION) {
            String nim = ""; 
            try {
                String[] data = {txt_id.getText()};
                crud.setDataKategori(data);
                crud.hapusData(crud.getDataKategori()[0]); 
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus",
                "Informasi", JOptionPane.INFORMATION_MESSAGE); tampilDatabase();
                resetText();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        }

    }//GEN-LAST:event_btn_hapusActionPerformed

