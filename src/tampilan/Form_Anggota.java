    private void tabel_AnggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_AnggotaMouseClicked
        txt_nim.setText(tabel_Anggota.getValueAt(tabel_Anggota.getSelectedRow(), 0).toString());
        txt_nama.setText(tabel_Anggota.getValueAt(tabel_Anggota.getSelectedRow(), 1).toString());
        txt_tempat_lahir.setText(tabel_Anggota.getValueAt(tabel_Anggota.getSelectedRow(), 2).toString());
        java.util.Date tgl_lhr=null;
        try{
            tgl_lhr = new SimpleDateFormat("yyyy-MM-dd").parse(tabMode.getValueAt(tabel_Anggota.getSelectedRow(), 3).toString());            
        }catch(ParseException ex){
            System.out.println(ex);
        }
        txt_tanggal_lahir.setDate(tgl_lhr);
        String jk = tabel_Anggota.getValueAt(tabel_Anggota.getSelectedRow(), 4).toString();
        if(jk.equals("Laki-Laki")) {
            rb_laki.setSelected(true);
        } else {
            rb_perempuan.setSelected(true);
        }
        txt_no_telp.setText(tabel_Anggota.getValueAt(tabel_Anggota.getSelectedRow(), 5).toString());
        txt_alamat.setText(tabel_Anggota.getValueAt(tabel_Anggota.getSelectedRow(), 6).toString());
    }//GEN-LAST:event_tabel_AnggotaMouseClicked

