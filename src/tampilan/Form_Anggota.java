    private void txt_cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cariKeyReleased
        // TODO add your handling code here:
        Object[] baris = {"NIM", "Nama", "Tempat Lahir", "Tanggal Lahir", "Jenis Kelamin", "No Telepon", "Alamat"};
        tabMode = new DefaultTableModel(null, baris); tabel_Anggota.setModel(tabMode);
        try {
            hasil = crud.cariData(txt_cari.getText()); 
            while (hasil.next()) {
                String[] data = {
                        hasil.getString("nim"),
                        hasil.getString("nama_anggota"),
                        hasil.getString("tempat_lahir"),
                        hasil.getString("tanggal_lahir"),
                        hasil.getString("jenis_kelamin"),
                        hasil.getString("no_telp"),
                        hasil.getString("alamat")
                    };
                crud.setDataAnggota(data);
                tabMode.addRow(crud.getDataAnggota());
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txt_cariKeyReleased

