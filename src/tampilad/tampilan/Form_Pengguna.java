    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        if (txt_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, ID Pengguna belum diisi"); txt_id.requestFocus();
        } else if (txt_nama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Nama belum diisi"); txt_nama.requestFocus();
        } else if (txt_username.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Username belum diisi"); txt_username.requestFocus();
        } else if (txt_password.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Password belum diisi"); txt_password.requestFocus();
        }else if (txt_telp.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, No. Telepon belum diisi"); txt_telp.requestFocus();
        }else if (txt_alamat.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, Alamat belum diisi"); txt_alamat.requestFocus();
        } else {
            try {
                String[] setData = {
                    txt_nama.getText(),
                    txt_username.getText(),
                    txt_password.getText(),
                    txt_telp.getText(),
                    txt_alamat.getText(),
                    txt_id.getText()
                };
                crud.setDataPengguna(setData);
                crud.ubahData(crud.getDataPengguna());
                JOptionPane.showMessageDialog(null, "Data Berhasil Ubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                tampilDatabase(); resetText();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Gagal diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

