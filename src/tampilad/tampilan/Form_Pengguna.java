        public void tampilDatabase() {
        Object[] baris = {"ID Pengguna", "Nama Pengguna", "Username", "Password", "No. Telepon", "Alamat"}; 
        tabMode = new DefaultTableModel(null, baris); 
        tabel_pengguna.setModel(tabMode);
            try {
               hasil = crud.tampilData();
                while (hasil.next()){
                    String[] data = {
                        hasil.getString("id"),
                        hasil.getString("nama_pengguna"),
                        hasil.getString("username"),
                        hasil.getString("password"),
                        hasil.getString("no_telp"),
                        hasil.getString("alamat")
                    };
                    crud.setDataPengguna(data);
                    tabMode.addRow(crud.getDataPengguna());
                }
            } catch (Exception e) {
            }      
    }
    
