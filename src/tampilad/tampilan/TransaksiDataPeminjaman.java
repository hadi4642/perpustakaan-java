    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        if(table_transaksi_peminjaman.getRowCount() == 3){
            JOptionPane.showMessageDialog(null, "Batas jumlah buku dipinjam : 3");
        }else{
            boolean data_ganda=false;
            int data = table_transaksi_peminjaman.getRowCount();
            for(int i=0;i<data;i++){
                if(tabel.getValueAt(i, 0).equals(cb_no_induk_buku.getSelectedItem())){
                    data_ganda = true;
                }
            }
            
            if(data_ganda){
                JOptionPane.showMessageDialog(null, "Data Sudah Dimasukkan Dalam Daftar !");
            }else if (cb_nim.getSelectedItem().toString().trim().equals("-")) {
                JOptionPane.showMessageDialog(null, "Maaf, Nim belum diisi"); cb_nim.requestFocus();
            } else if(cb_no_induk_buku.getSelectedItem().toString().trim().equals("-")){
                JOptionPane.showMessageDialog(null, "Maaf, No Induk Buku belum diisi"); cb_no_induk_buku.requestFocus();
            }else {
                try {
                    String[] tabel_master={cb_no_induk_buku.getSelectedItem().toString(),txt_judul_buku.getText(),txt_pengarang.getText(),txt_tersedia.getText(),txt_keluar.getText()};
                    
                    tabel.addRow(tabel_master);

                    cb_no_induk_buku.setSelectedItem("-");
                    txt_judul_buku.setText(null);
                    txt_pengarang.setText(null);
                    txt_jumlah_buku.setText("0");
                    txt_tersedia.setText("0");
                    txt_keluar.setText("0");
                    
                    txt_jumlah_buku.setText(""+table_transaksi_peminjaman.getRowCount());
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                }
            } 
        } 
    }//GEN-LAST:event_btn_tambahActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        int tabel_data = table_transaksi_peminjaman.getSelectedRow();        
        if(String.valueOf(tabel_data).equals("-1")){
            JOptionPane.showMessageDialog(this, "Pilih Daftar Yang Akan Dihapus !");
        }else
        {
            tabel.removeRow(tabel_data);
            cb_nim.requestFocus();
        }
        txt_jumlah_buku.setText(""+table_transaksi_peminjaman.getRowCount());
    }//GEN-LAST:event_btn_hapusActionPerformed

    
    
