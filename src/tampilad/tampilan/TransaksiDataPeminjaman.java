    private void tampilTanggal() {
        SimpleDateFormat sdfdate = new SimpleDateFormat("yyyy-MM-dd");
        Date tanggal_sekarang = new Date();
        txt_tanggal_pinjam.setText(sdfdate.format(tanggal_sekarang));
        
        Calendar cal = Calendar.getInstance();
        cal.add(Calendar.DATE, 6);
        Date tigaharilagi = cal.getTime();
        txt_batas_pengembalian.setText(sdfdate.format(tigaharilagi));
    }
    
    public void tampilAnggota() {
            try {
               hasil = crud.tampilAnggota();
                while (hasil.next()){
                    cb_nim.addItem(hasil.getString(1));
                }
            } catch (Exception e) {
                
            }      
    }
    
    public void tampilBuku() {
        try {
           hasil = crud.tampilBuku();
            while (hasil.next()){
                cb_no_induk_buku.addItem(hasil.getString(1));
            }
        } catch (Exception e) {

        }      
    }
    
