    private void cb_nimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_nimActionPerformed
        // TODO add your handling code here:
        boolean cekAnggota = crud.checkAnggota(cb_nim.getSelectedItem().toString());
        if(cekAnggota){
            JOptionPane.showMessageDialog(null, "Anggota Masih Memiliki \nTanggungan Peminjaman Buku!");
        }else{
            try {
                hasil = crud.getAnggotaByNim(cb_nim.getSelectedItem().toString());
                 while (hasil.next()){
                     txt_nama_anggota.setText(hasil.getString("nama_anggota"));
                     txt_no_telp.setText(hasil.getString("no_telp"));
                 }
             } catch (Exception e) {

             } 
        }  
    }//GEN-LAST:event_cb_nimActionPerformed

    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        
        if(cb_nim.getSelectedItem().toString().trim().equals("-") || txt_jumlah_buku.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Lengkapi Data Peminjaman Terlebih Dahulu !");
        }else {
            try {
                String[] setData = {
                    txt_no_peminjaman.getText(),
                    cb_nim.getSelectedItem().toString(),
                    txt_tanggal_pinjam.getText(),
                    txt_batas_pengembalian.getText(),
                };
                crud.setDataPeminjaman(setData);
                crud.simpanData(crud.getDataPeminjaman());
                crud.simpanDataDetail(txt_no_peminjaman.getText(),tabel, table_transaksi_peminjaman.getRowCount());
                crud.updateStokBuku(tabel, table_transaksi_peminjaman.getRowCount());
                JOptionPane.showMessageDialog(null, "Data Berhasil Tersimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                txt_no_peminjaman.setText(crud.penomoranOtomatis());
                btn_resetActionPerformed(evt);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

