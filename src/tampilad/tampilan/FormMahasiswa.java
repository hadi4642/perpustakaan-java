    public void tampilDatabase() {
        Object[] baris = {"NIM", "Nama", "Jenis Kelamin", "Alamat"}; 
        tabMode = new DefaultTableModel(null, baris); 
        tabel_mahasiswa.setModel(tabMode);
            try {
               hasil = crud.tampilData();
                while (hasil.next()){
                    String[] data = {
                        hasil.getString("nim"),
                        hasil.getString("nama"),
                        hasil.getString("jenis_kelamin"),
                        hasil.getString("alamat")
                    };
                    crud.setDataMahasiswa(data);
                    tabMode.addRow(crud.getDataMahasiswa());
                }
            } catch (Exception e) {
            }      
    }
    
