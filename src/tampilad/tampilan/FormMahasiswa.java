    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        if (txt_nim.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, NIM belum diisi"); txt_nim.requestFocus();
        } else if (txt_nama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf Nama belum diisi"); txt_nama.requestFocus();
        } else if (txt_alamat.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf Alamat belum diisi"); txt_alamat.requestFocus();
        } else {
            try {
                String[] setData = {
                    txt_nim.getText(),
                    txt_nama.getText(),
                    bg.getSelection().getActionCommand(),
                    txt_alamat.getText()
                };
                crud.setDataMahasiswa(setData);
                crud.simpanData(crud.getDataMahasiswa());
                JOptionPane.showMessageDialog(null, "Data Berhasil Tersimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                tampilDatabase(); resetText();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_btn_simpanActionPerformed

